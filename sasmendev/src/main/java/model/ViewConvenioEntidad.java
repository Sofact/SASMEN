package model;

public class ViewConvenioEntidad {
	
	private int coenId;
	private int convId;
	private int entId;
	private String entiNombre;
	public int getCoenId() {
		return coenId;
	}
	public void setCoenId(int coenId) {
		this.coenId = coenId;
	}
	public int getConvId() {
		return convId;
	}
	public void setConvId(int convId) {
		this.convId = convId;
	}
	public int getEntId() {
		return entId;
	}
	public void setEntId(int entId) {
		this.entId = entId;
	}
	public String getEntiNombre() {
		return entiNombre;
	}
	public void setEntiNombre(String entiNombre) {
		this.entiNombre = entiNombre;
	}
	

}
