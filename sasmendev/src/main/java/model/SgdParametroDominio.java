package model;

public class SgdParametroDominio {

	
	private int padoId;
	private String padoNombre;
	private String padoDescripcion;
	private String padoEstado;
	
	public int getPadoId() {
		return padoId;
	}
	public void setPadoId(int padoId) {
		this.padoId = padoId;
	}
	public String getPadoNombre() {
		return padoNombre;
	}
	public void setPadoNombre(String padoNombre) {
		this.padoNombre = padoNombre;
	}
	public String getPadoDescripcion() {
		return padoDescripcion;
	}
	public void setPadoDescripcion(String padoDescripcion) {
		this.padoDescripcion = padoDescripcion;
	}
	public String getPadoEstado() {
		return padoEstado;
	}
	public void setPadoEstado(String padoEstado) {
		this.padoEstado = padoEstado;
	}
}
